function getRandomCompliment() {
    const randomIndex = Math.floor(Math.random() * compliments.length);
    return compliments[randomIndex];
}

function revealCompliment() {
    const complimentText = document.getElementById('complimentText');
    const lastCompliment = localStorage.getItem('lastCompliment');
    if (lastCompliment) {
        complimentText.textContent = lastCompliment;
    }
}

function showCompliment() {
    const complimentText = document.getElementById('complimentText');
    const newCompliment = getRandomCompliment();
    complimentText.textContent = "...×œ×—×¦×™ ×›×“×™ ×œ×¨××•×ª";
    localStorage.setItem('lastCompliment', newCompliment);
    complimentText.setAttribute('data-revealed', 'false');
}

function manualUpdate() {
    showCompliment();
}

function checkHourlyUpdate() {
    const currentHour = new Date().getHours();
    const lastUpdateHour = localStorage.getItem('lastUpdateHour');
    if (currentHour !== lastUpdateHour) {
        showCompliment();
        localStorage.setItem('lastUpdateHour', currentHour);
    }
}

// Show the "tap to see" message when the page loads
window.onload = () => {
    const complimentText = document.getElementById('complimentText');
    const lastCompliment = localStorage.getItem('lastCompliment');
    if (lastCompliment) {
        complimentText.textContent = "...×œ×—×¦×™ ×›×“×™ ×œ×¨××•×ª";
        complimentText.setAttribute('data-revealed', 'false');
    } else {
        showCompliment();
    }
    checkHourlyUpdate();
    complimentText.addEventListener('click', () => {
        if (complimentText.getAttribute('data-revealed') === 'false') {
            complimentText.textContent = localStorage.getItem('lastCompliment');
            complimentText.setAttribute('data-revealed', 'true');
        }
    });
}

// Check for hourly updates every minute
setInterval(checkHourlyUpdate, 60000);